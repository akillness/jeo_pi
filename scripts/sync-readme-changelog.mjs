#!/usr/bin/env node
// Sync the N most recent release sections from CHANGELOG.md into README.md
// between the <!-- CHANGELOG:START --> / <!-- CHANGELOG:END --> markers.
//
// The full history lives only in CHANGELOG.md; README shows a trimmed window so
// it never drifts into a second, hand-maintained changelog. Run via
// `npm run changelog:sync` or automatically during semantic-release `prepare`.
//
// Usage: node scripts/sync-readme-changelog.mjs [count]

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const changelogPath = join(repoRoot, "CHANGELOG.md");
const readmePath = join(repoRoot, "README.md");

const START = "<!-- CHANGELOG:START -->";
const END = "<!-- CHANGELOG:END -->";

const count = Number.parseInt(process.argv[2] ?? "5", 10);
if (!Number.isInteger(count) || count <= 0) {
  console.error(`sync-readme-changelog: invalid count "${process.argv[2]}" (expected a positive integer)`);
  process.exit(1);
}

const changelog = readFileSync(changelogPath, "utf8");
const lines = changelog.split("\n");

// A release section starts at a level-2 version header, e.g. "## [1.22.0](...)".
const headerRe = /^## \[/;
const startIdxs = [];
for (let i = 0; i < lines.length; i++) {
  if (headerRe.test(lines[i])) startIdxs.push(i);
}

if (startIdxs.length === 0) {
  console.error("sync-readme-changelog: no '## [version]' release sections found in CHANGELOG.md");
  process.exit(1);
}

const taken = startIdxs.slice(0, count);
const sliceStart = taken[0];
const sliceEnd = startIdxs[count] ?? lines.length; // exclusive
const sectionLines = lines.slice(sliceStart, sliceEnd);

// Demote headings one level so the embedded sections nest cleanly under the
// README's own "## Changelog" heading (deepest first to avoid double-demoting).
const demoted = sectionLines.map((line) =>
  line.replace(/^### /, "#### ").replace(/^## /, "### "),
);

const body = demoted.join("\n").trim();
const block = [
  START,
  "<!-- Generated by scripts/sync-readme-changelog.mjs — do not edit by hand; run `npm run changelog:sync`. -->",
  "",
  body,
  "",
  "_See [`CHANGELOG.md`](CHANGELOG.md) for the complete release history._",
  END,
].join("\n");

const readme = readFileSync(readmePath, "utf8");
const startPos = readme.indexOf(START);
const endPos = readme.indexOf(END);
if (startPos === -1 || endPos === -1 || endPos < startPos) {
  console.error(
    `sync-readme-changelog: README.md is missing the ${START} / ${END} markers`,
  );
  process.exit(1);
}

const updated = readme.slice(0, startPos) + block + readme.slice(endPos + END.length);
if (updated === readme) {
  console.log(`sync-readme-changelog: README.md already up to date (latest ${taken.length} release(s)).`);
} else {
  writeFileSync(readmePath, updated);
  console.log(`sync-readme-changelog: README.md updated with the latest ${taken.length} release(s).`);
}
